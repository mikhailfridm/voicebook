def gen_multi_person():
    v = pick_v(); vd = VERTICALS[v]
    svc = rc(vd["services"]); name1 = rc(NAMES); name2 = rc(NAMES)
    date,time = rc(DATES),rc(TIMES)
    c = [S(v), A(rc(vd["greets"]))]

    pattern = rc(["handoff","consulting","booking_for_two"])
    if pattern == "handoff":
        c.append(U(f"Здравствуйте, подождите, я передам трубку... [пауза]"))
        c.append(A("Конечно, жду!"))
        c.append(U(f"Алло? Здравствуйте, это {name2}. Я хочу записаться на {svc}"))
        c.append(A(f"Здравствуйте, {name2}! Когда удобно?"))
        c.append(U(f"{date} {time}"))
    elif pattern == "consulting":
        c.append(U(f"Здравствуйте, хочу записаться на {svc}... [в сторону: Маш, мне во вторник или в среду?]... подождите секунду"))
        c.append(A("Конечно, не торопитесь!"))
        c.append(U(f"[в сторону: ну давай в среду]... Давайте в среду, {time}"))
        c.append(A(f"Хорошо, в среду, {time}! На чьё имя?"))
        c.append(U(f"На {name1}... [в сторону: или на тебя записать?]... нет, на {name1}"))
    else:
        c.append(U(f"Здравствуйте, хочу записать двоих — меня и подругу. На {svc}"))
        c.append(A(f"Конечно! Можем записать друг за другом. Когда удобно?"))
        c.append(U(f"{date} {time}"))
        c.append(A(f"Как зовут вас и подругу?"))
        c.append(U(f"Я {name1}, подруга {name2}"))

    c.append(A(f"Записала! Ждём вас {date}!"))
    c.append(U("Спасибо большое!"))
    c.append(A("До свидания! Хорошего дня!"))
    return c

